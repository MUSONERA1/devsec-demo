"""
Password Reset Security Tests

This test suite verifies that the password reset flow is secure and user-friendly.

Security Focus:
- Tokens are secure (HMAC-based, generated by Django)
- Tokens expire after PASSWORD_RESET_TIMEOUT (default 3 days)
- Tokens are one-time use (consumed after reset)
- No user enumeration via error messages
- Password validation enforced
- CSRF protection enabled
- Secure token delivery via email
- Old sessions remain valid after reset

Test Coverage:
- Password reset request flow
- Token generation and validation
- Password reset confirmation
- Password validation rules
- User enumeration prevention
- Token expiration
- Invalid token handling
- Existing functionality preservation
"""

